def has_seven(k):
    """Returns True if at least one of the digits of k is a 7, False otherwise.

    >>> has_seven(3)
    False
    >>> has_seven(7)
    True
    >>> has_seven(2734)
    True
    >>> has_seven(2634)
    False
    >>> has_seven(734)
    True
    >>> has_seven(7777)
    True
    """
    if k % 10 == 7:
        return True
    elif k < 10:
        return False
    else:
        return has_seven(k // 10)

def count_change(amount):
    """Return the number of ways to make change for amount.

    >>> count_change(7)
    6
    >>> count_change(10)
    14
    >>> count_change(20)
    60
    >>> count_change(100)
    9828
    """
    def cent(total, coin):
        if total == 0:
            return 1
        elif total < 0:
            return 0
        elif coin == 0:
            return 0
        else:
            with_coin = cent(total - coin, coin)
            without_coin = cent(total, coin // 2)
            return with_coin + without_coin
    return cent(amount, 2 ** (log(amount, 2) // 1))

